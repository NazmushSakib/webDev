//************ 021 & 023_if-else condition ***********
var a = "20";
var b = 20;

if (a > b) {
  console.log(a + " is greater than " + b);
} else if (a < b) {
  console.log(a + " is less than " + b);
} else if (a === b) {
  console.log("Both are equal.");
} else {
  console.log("Wrong Entry.");
}

//*********** 022_Even-Odd number *************
var n = 63;

if (n % 2 === 0) {
  console.log(n + " is an even number.");
} else {
  console.log(n + " is an odd number.");
}
